package capitulo2.operator;

public class OperadoresUniarios {
    public static void main(String[] args) {
        int a = 4;
        ++a;
        System.out.println("Valor de ++a: " + a);
        a++;
        System.out.println("Valor de a++: " + a);
        --a;
        System.out.println("Valor de --a: " + a);
        a--;
        System.out.println("Valor de a--: " + a);
    }
}