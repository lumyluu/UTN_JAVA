package capitulo2.moduloUno_Cap2;

public class ExplorerPrimitiveType {

    public static void main (String [] args  ) {

        // Declaration and initialization of the INT type

        int a;
        int h= 10;

        //Conversion of the type (cast) in the integers type
        long p = 10l;
        long n = (long) 10;
        short s = (short) 5;
        byte b = (byte) 1;

        //Example

        int x = 1;
        double j = 2.5;

        x = (int) j;

        System.out.println(x);

        //Conversion of the type in the point float

        float f = 10.5F;
        float g = (float) 10.5;

        //Text type
        // Assignment of a character
        char c = 'a';

        // boolean type
        //true or false

        boolean k = false;


    }
}
